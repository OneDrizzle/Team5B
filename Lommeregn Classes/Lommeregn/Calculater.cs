using System;
using System.Collections.Generic;
using System.Text;

namespace Lommeregn
{
    public class Calculater
    {
        public int Add(int tal1, int tal2)
        {
            return tal1 + tal2;
        }
        public int Subtract(int tal1, int tal2)
        {
            return tal1 - tal2;
        }
        public double Devide(double tal1, double tal2)
        {
            return tal1 / tal2;
        }
        public int Multiply(int tal1, int tal2)
        {
            return tal1 * tal2;
        }
    }
}
